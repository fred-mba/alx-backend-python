#!/usr/bin/env python3
"""
A module that takes a value of n and returns the string rep of the float.
"""


def to_str(n: float) -> str:
    """
    Returns str representantion of the float.

    Parameters
    ----------
    n(float): The floating point number.

    Returns
    -------
    str: The string value of n.
    """
    return repr(n)
